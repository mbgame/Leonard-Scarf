<h2 class="rules"><strong>شال نخی</strong></h2>
<h3 class="content">انعطاف زیادی که در بین بافت های شال  نخی لئونارد وجود دارد باعث نرمی دوخت پارچه و خوش حالت بودن آن می شود.
از همین رو در فصل های تابستان شرکت شال و روسری لئونارد بسیاری از طرحهای خود را به این نوع  پارچه اختصاص میدهد.
در شال های  نخی  لئونارد پارچه های خنکی بکار رفته که استفاده از آنها نه تنها باعث می شود گرمای تابستان را تحمل کنید بلکه با کوچکترین نسیمی , از عطر و رایحه فصل زیبای تابستان لذت برده و از خنک شدن سر و گردن خود لذت ببرید...
</h3>
