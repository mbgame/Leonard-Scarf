<h2 class="rules"><strong>روسری ابریشم</strong></h2>
<h3 class="content">بافتن و تولید پارچه های ابریشمی در زمان های قدیم یک راز محسوب میشده و این پارچه پر خرج و گرانبها را که با مشقت و هزینه ای گزاف تولید میشد تنها محدود به استفاده در خانواده های اشراف می شد، اما با گذشت زمان و فراگیر شدن دانش چگونگی تولید این پارچه,  استفاده از آن از انحصار خانواده های سلطنتی خارج شده و  به یک کالای لوکس عمومی تبدیل شود.تمامی انواع مختلف پارچه شال و روسری ابریشم لئونارد از پیله های کرم ابریشم بدست می آید، اما فرایند پردازش و بافت و پرداخت این پارچه در مدلهای مختلف متفاوت می باشدو به همین دلیل تفاوتهای زیادی بین اونواع محصولات ابریشمی دیده می شود و شرکت لئونارد یکی از باکیفیت ترین و لطیف ترین انواع این پارچه را بصورت شال و روسری در اختیار شما بانوان محترم ایران قرار داده است.</h3>
