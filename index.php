<h2 class="rules">پیشنهاد ما به شما برای خرید شال و روسری</h2>
<h3 class="content">شال یا روسری که قصد خریدش را دارید می بایست دارای کیفیت خوبی بوده تا ظاهر شمارا آراسته کند. جنس پارچه انتخاب شده باید بگونه ای باشد که مدت زمان معقولی عمر کند پس حتما قبل از خرید به بافت و جنس آن توجه کنید. لئونارد با توجه به تجربه ای درخشان و موفق در زمینه طراحی و تولید شال و روسری همواره سعی کرده است که تمامی محصولات خود را با بالاترین کیفیت مطلوب ,در اختیار شما مشتریان محترم قرار دهد از نشانه های شال یا روسری خوب می توان گفت که بهتر است جنس آن از هردوطرف کیفیت مطلوبی داشته باشد، هرچه بافت آن نرم و در عین حال مقاوم تر باشد ارزش آن بالاتر است. کسانی در انتخاب شال و روسری موفق ترند که هنگام خرید از شکل، فرم و رنگ پوست خود آگاهی داشته باشند و بدانند بر چه اساسی می بایست شال و روسری خود را انتخاب کنند. و این امر را لئونارد برای شما فراهم کرده است ,اگر قدی بلند و اندامی درشت دارید از روسری های تک رنگ و زیبای لئونارد (ترجیحا تیره) استفاده کنید تا شمارا لاغرتر نشان دهد. اگر کوتاه قد هستید و اندامی لاغر دارید بهتر است محور انتخاب روسری یا شال خود را برمبنای یک رنگ روشن و با ترکیبی از چند رنگ شاد که با رنگ اصلی  هارمونی داشته باشد, انتخاب بفرمائید.		</h3>
